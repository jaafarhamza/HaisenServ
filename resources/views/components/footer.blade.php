    // Back to top button functionality
    function setupBackToTop() {
        const backToTopButton = document.getElementById('back-to-top');

        if (backToTopButton) {
            // Show button when page is scrolled down
            window.addEventListener('scroll', () => {
                if (window.pageYOffset > 300) {
                    backToTopButton.classList.remove('opacity-0', 'invisible');
                    backToTopButton.classList.add('opacity-100', 'visible');
                } else {
                    backToTopButton.classList.add('opacity-0', 'invisible');
                    backToTopButton.classList.remove('opacity-100', 'visible');
                }
            });

            // Scroll to top when button is clicked
            backToTopButton.addEventListener('click', () => {
                window.scrollTo({
                    top: 0,
                    behavior: 'smooth'
                });
            });
        }
    }

    // Animated counters for statistics
    function animateCounters() {
        const counters = document.querySelectorAll('[data-count]');
        const options = {
            root: null,
            threshold: 0.5,
            rootMargin: '0px'
        };

        const observer = new IntersectionObserver((entries) => {
            entries.forEach(entry => {
                if (entry.isIntersecting) {
                    const counter = entry.target;
                    const target = parseInt(counter.getAttribute('data-count'));
                    const currentText = counter.textContent.trim();
                    const currentValue = parseInt(currentText);

                    // Only animate if the counter hasn't been animated already
                    if (currentValue !== target) {
                        // If counter is already set to "X+" format, extract the number
                        const startValue = currentText.endsWith('+') ?
                            parseInt(currentText.replace('+', '')) :
                            0;

                        const duration = 2000;
                        const step = (target - startValue) / (duration / 16);
                        let current = startValue;

                        const timer = setInterval(() => {
                            current += step;
                            if (current >= target) {
                                counter.textContent = target + '+';
                                clearInterval(timer);
                            } else {
                                counter.textContent = Math.floor(current) + '+';
                            }
                        }, 16);
                    }

                    // Unobserve after animation
                    observer.unobserve(counter);
                }
            });
        }, options);

        counters.forEach(counter => {
            observer.observe(counter);
        });
    }

    // Theme toggle in footer
    function setupFooterThemeToggle() {
        const themeToggle = document.getElementById('theme-toggle-footer');
        const toggleDot = document.getElementById('theme-toggle-dot');

        if (themeToggle && toggleDot) {
            // Set initial position based on current theme
            if (document.documentElement.classList.contains('dark')) {
                toggleDot.classList.add('translate-x-8');
            } else {
                toggleDot.classList.remove('translate-x-8');
            }

            themeToggle.addEventListener('click', () => {
                // Toggle dark class on html element
                document.documentElement.classList.toggle('dark');

                // Move the toggle dot
                toggleDot.classList.toggle('translate-x-8');

                // Save preference
                if (document.documentElement.classList.contains('dark')) {
                    localStorage.setItem('theme', 'dark');
                } else {
                    localStorage.setItem('theme', 'light');
                }

                // Update header toggle buttons to match
                updateHeaderThemeButtons();
            });
        }
    }

    // Update header theme buttons to match current theme
    function updateHeaderThemeButtons() {
        const desktopToggle = document.getElementById('dark-mode-toggle-desktop');
        const mobileToggle = document.getElementById('dark-mode-toggle-mobile');

        if (desktopToggle) {
            const sunIcon = document.getElementById('sun-icon-desktop');
            const moonIcon = document.getElementById('moon-icon-desktop');

            if (document.documentElement.classList.contains('dark')) {
                sunIcon.classList.remove('hidden');
                moonIcon.classList.add('hidden');
            } else {
                sunIcon.classList.add('hidden');
                moonIcon.classList.remove('hidden');
            }
        }

        if (mobileToggle) {
            const sunIcon = document.getElementById('sun-icon-mobile');
            const moonIcon = document.getElementById('moon-icon-mobile');

            if (document.documentElement.classList.contains('dark')) {
                sunIcon.classList.remove('hidden');
                moonIcon.classList.add('hidden');
            } else {
                sunIcon.classList.add('hidden');
                moonIcon.classList.remove('hidden');
            }
        }
    }

    // Run setup when DOM is loaded
    document.addEventListener('DOMContentLoaded', () => {
        setupBackToTop();
        animateCounters();
        setupFooterThemeToggle();

        // If theme changes via header buttons, update footer toggle
        const desktopToggle = document.getElementById('dark-mode-toggle-desktop');
        const mobileToggle = document.getElementById('dark-mode-toggle-mobile');

        if (desktopToggle) {
            desktopToggle.addEventListener('click', () => {
                const toggleDot = document.getElementById('theme-toggle-dot');
                if (toggleDot) {
                    toggleDot.classList.toggle('translate-x-8');
                }
            });
        }

